#ifndef   __EX00_H__
# define  __EX00_H__

template<typename T>
void		swap(T &a, T &b)
{
  T		tmp = a;

  a = b;
  b = tmp;
}

template<typename T>
T		max(const T &a, const T &b)
{
  if (a > b)
    return a;
  return b;
}

template<typename T>
T		min(const T &a, const T &b)
{
  if (a < b)
    return a;
  return b;
}

template<typename T>
T		add(const T &a, const T &b)
{
  return a + b;
}

#endif     /* !__EX00_H__ */
