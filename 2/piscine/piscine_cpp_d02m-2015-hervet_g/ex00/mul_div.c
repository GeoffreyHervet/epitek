void add_mul_4param(int first, int second, int *add, int *mul)
{
  *add = first + second;
  *mul = first * second;
}

void add_mul_2param(int *first, int *second)
{
  int	res1;
  res1 = *first + *second;
  *second = *second * *first;
  *first = res1;
}

